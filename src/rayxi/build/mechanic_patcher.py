"""mechanic_patcher — wire HLR mechanic_specs into an already-generated Godot project.

The rest of the codegen produces template-baseline Godot files (fighter.gd,
combat_system.gd, fighting.gd, etc.). This module takes those files and
auto-patches them so custom mechanic_specs are actually wired up. Zero
hand-written code per feature — everything is derived from the spec.

Operations performed per mechanic_spec:
  1. Inject fighter-role properties into every character .gd file as state vars
  2. Instantiate the mechanic system node in the gameplay scene (.gd)
  3. Instantiate every HUD widget + wire fighter_path
  4. Call mechanic_system.setup(fighters) + .process(delta) in the scene loop
  5. For mechanic_specs that react to combat damage, patch combat_system.gd
     to call `mechanic_system.process_damage_event(defender, damage)` after
     damage is applied

Idempotent: every patch checks for its own marker comment before inserting,
so running the patcher multiple times is safe.
"""

from __future__ import annotations

import logging
import re
from pathlib import Path

from rayxi.spec.models import GameIdentity, MechanicSpec

_log = logging.getLogger("rayxi.build.mechanic_patcher")


# ---------------------------------------------------------------------------
# Type coercion: mechanic_spec property type string → GDScript (type, default)
# ---------------------------------------------------------------------------


def _gd_type(spec_type: str) -> str:
    t = spec_type.lower()
    return {
        "int": "int", "integer": "int",
        "float": "float", "number": "float",
        "bool": "bool", "boolean": "bool",
        "string": "String",
        "vector2": "Vector2",
        "color": "Color",
        "rect2": "Rect2",
        "list": "Array",
        "dict": "Dictionary",
    }.get(t, "Variant")


def _default_for_type(spec_type: str) -> str:
    t = spec_type.lower()
    return {
        "int": "0", "integer": "0",
        "float": "0.0", "number": "0.0",
        "bool": "false", "boolean": "false",
        "string": '""',
        "vector2": "Vector2.ZERO",
        "color": "Color.WHITE",
        "rect2": "Rect2()",
        "list": "[]",
        "dict": "{}",
    }.get(t, "null")


# ---------------------------------------------------------------------------
# Patch 1: character .gd fighter-property injection
# ---------------------------------------------------------------------------


def _existing_var_names(gd_source: str) -> set[str]:
    """Return the set of `var <name>` declarations already in a GDScript file.
    Catches both `var x` and `var x: Type = value`. Ignores `const` and method
    names. We use this to avoid re-declaring properties that the hand-written
    template already defines.
    """
    names: set[str] = set()
    for line in gd_source.splitlines():
        stripped = line.strip()
        if stripped.startswith("var ") or stripped.startswith("@export var "):
            # var name[: Type] [= value]
            m = re.match(r"(?:@export\s+)?var\s+([a-zA-Z_][a-zA-Z0-9_]*)", stripped)
            if m:
                names.add(m.group(1))
    return names


# Native members inherited from Node/Node2D/CollisionObject2D/CharacterBody2D
# that must NEVER be redeclared as `var` (Godot throws "Member redefined").
# This list is a small game-agnostic blacklist — any character class that
# extends a Node2D descendant gets these for free.
_GODOT_NATIVE_MEMBERS = frozenset({
    "position", "rotation", "rotation_degrees", "scale", "skew",
    "transform", "global_position", "global_transform", "global_rotation",
    "z_index", "z_as_relative", "y_sort_enabled",
    "visible", "modulate", "self_modulate", "show_behind_parent",
    "velocity", "up_direction", "slide_on_ceiling", "floor_stop_on_slope",
    "floor_constant_speed", "floor_block_on_wall", "floor_max_angle",
    "floor_snap_length", "wall_min_slide_angle", "motion_mode",
    "platform_on_leave", "platform_floor_layers", "platform_wall_layers",
    "collision_layer", "collision_mask", "collision_priority",
    "name", "owner", "script", "process_mode", "process_priority",
    "process_physics_priority", "process_thread_group", "unique_name_in_owner",
})


def _inject_imap_fighter_properties(char_file: Path, imap) -> list[str]:
    """DEPRECATED — character files are now fully generated by
    build/character_gen.py from the impact map, so there's nothing to inject.
    Kept as a no-op to preserve the patches[] manifest shape.
    """
    return []


def _inject_fighter_properties(char_file: Path, specs: list[MechanicSpec]) -> list[str]:
    """Add mechanic_spec fighter-role properties as `var` declarations.

    Inserted right before the first `func _ready` in the file. Idempotent via
    a per-spec marker comment. Skips property names that are already declared
    in the file (e.g. from _inject_imap_fighter_properties running first).
    """
    content = char_file.read_text(encoding="utf-8")
    existing = _existing_var_names(content)
    applied: list[str] = []
    blocks: list[str] = []

    for spec in specs:
        fighter_props = [p for p in spec.properties if p.role in ("fighter", "character")]
        if not fighter_props:
            continue
        marker = f"# --- {spec.system_name} (mechanic_spec, injected by mechanic_patcher) ---"
        if marker in content:
            continue  # already patched in a prior run

        fresh_props = [p for p in fighter_props if p.name not in existing]
        if not fresh_props:
            continue

        lines = ["", marker]
        for p in fresh_props:
            lines.append(f"var {p.name}: {_gd_type(p.type)} = {_default_for_type(p.type)}")
            existing.add(p.name)
        blocks.append("\n".join(lines))
        applied.append(f"{spec.system_name}: {len(fresh_props)} fighter props")

    if not blocks:
        return applied

    insertion = "\n".join(blocks) + "\n\n"
    if "func _ready" in content:
        content = content.replace("func _ready", insertion + "func _ready", 1)
    else:
        content = content.rstrip() + "\n\n" + insertion

    char_file.write_text(content, encoding="utf-8")
    return applied


# ---------------------------------------------------------------------------
# Patch 2/3/4: fighting scene wiring — instantiate mechanic systems + HUDs
# ---------------------------------------------------------------------------


def _wire_fighting_scene(
    scene_gd: Path,
    specs: list[MechanicSpec],
    hud_scripts_rel_path: str = "res://scripts/hud",
    system_scripts_rel_path: str = "res://scripts/systems",
) -> list[str]:
    """Scene wiring is now owned by build/scene_gen.py, which emits systems +
    HUD widgets from imap.systems + hlr.mechanic_specs in one deterministic
    pass. This function is a no-op so the mechanic_patcher pipeline still
    returns a patches[] manifest of the OTHER patches it does (fighter props
    injection, combat damage hook, etc.) without double-wiring the scene.
    """
    return []


def _spec_reacts_to_damage(spec: MechanicSpec) -> bool:
    """Return True if any of the spec's interactions are triggered by combat damage."""
    for it in spec.interactions:
        trig = (it.trigger or "").lower()
        if "combat_system" in trig and "damage" in trig:
            return True
    return False


