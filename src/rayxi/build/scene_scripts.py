"""Scene-specific scripts — makes each screen actually work.

Start Screen: show title, blink "PRESS SPACE", transition to char_select on SPACE.
Char Select:  show character grid, move cursor, confirm, CPU auto-picks, transition to fighting.
Fighting:     wire up systems (generated separately).
Round Over:   show result, wait for SPACE.
Match Over:   show winner, return to start.

Each script is self-contained — no external dependencies beyond Godot built-ins.
"""

from __future__ import annotations

from pathlib import Path


def generate_start_screen_script(game_name: str) -> str:
    return f"""extends Node2D
## Start Screen — press SPACE to begin.
## Generated by RayXI for: {game_name}

var blink_timer: float = 0.0
var blink_visible: bool = true

func _ready():
\t# Create title
\tvar title = Label.new()
\ttitle.name = "TitleLabel"
\ttitle.text = "{game_name.upper().replace('_', ' ')}"
\ttitle.position = Vector2(960, 300)
\ttitle.add_theme_font_size_override("font_size", 72)
\ttitle.horizontal_alignment = HORIZONTAL_ALIGNMENT_CENTER
\ttitle.set_anchors_preset(Control.PRESET_CENTER_TOP)
\tadd_child(title)
\t
\tvar prompt = Label.new()
\tprompt.name = "PromptLabel"
\tprompt.text = "PRESS SPACE TO START"
\tprompt.position = Vector2(960, 600)
\tprompt.add_theme_font_size_override("font_size", 36)
\tprompt.horizontal_alignment = HORIZONTAL_ALIGNMENT_CENTER
\tprompt.set_anchors_preset(Control.PRESET_CENTER_TOP)
\tadd_child(prompt)
\t
\tprint("Start screen ready")

func _process(delta):
\t# Blink the prompt
\tblink_timer += delta
\tif blink_timer >= 0.5:
\t\tblink_timer = 0.0
\t\tblink_visible = not blink_visible
\t\tvar prompt = get_node_or_null("PromptLabel")
\t\tif prompt:
\t\t\tprompt.visible = blink_visible
\t
\t# SPACE to start
\tif Input.is_action_just_pressed("ui_accept"):
\t\tvar main = get_parent()
\t\tif main and main.has_method("transition_to"):
\t\t\tmain.transition_to("char_select")
"""


def generate_char_select_script(game_name: str, characters: list[str]) -> str:
    char_array = ", ".join(f'"{c}"' for c in characters)
    return f"""extends Node2D
## Character Select — pick your fighter.
## Generated by RayXI for: {game_name}

var characters: Array = [{char_array}]
var cursor_index: int = 0
var p1_confirmed: bool = false
var p2_confirmed: bool = false
var cpu_timer: float = 0.0
var transition_timer: float = -1.0

# UI nodes created in _ready
var char_labels: Array = []
var cursor_rect: ColorRect
var p1_pick_label: Label
var p2_pick_label: Label
var status_label: Label

func _ready():
\t# Background
\tvar bg = ColorRect.new()
\tbg.color = Color(0.1, 0.1, 0.15, 1)
\tbg.size = Vector2(1920, 1080)
\tadd_child(bg)
\t
\t# Title
\tvar title = Label.new()
\ttitle.text = "SELECT YOUR FIGHTER"
\ttitle.position = Vector2(960, 80)
\ttitle.add_theme_font_size_override("font_size", 48)
\ttitle.horizontal_alignment = HORIZONTAL_ALIGNMENT_CENTER
\ttitle.set_anchors_preset(Control.PRESET_CENTER_TOP)
\tadd_child(title)
\t
\t# Character grid
\tvar grid_start_x = (1920 - (len(characters) * 250)) / 2.0
\tfor i in range(len(characters)):
\t\tvar panel = ColorRect.new()
\t\tpanel.color = Color(0.2, 0.2, 0.3, 1)
\t\tpanel.position = Vector2(grid_start_x + i * 250, 250)
\t\tpanel.size = Vector2(200, 300)
\t\tadd_child(panel)
\t\t
\t\tvar lbl = Label.new()
\t\tlbl.text = characters[i].to_upper()
\t\tlbl.position = Vector2(grid_start_x + i * 250 + 20, 560)
\t\tlbl.add_theme_font_size_override("font_size", 28)
\t\tadd_child(lbl)
\t\tchar_labels.append(lbl)
\t
\t# Cursor (highlight box)
\tcursor_rect = ColorRect.new()
\tcursor_rect.name = "Cursor"
\tcursor_rect.color = Color(1, 1, 0, 0.3)
\tcursor_rect.position = Vector2(grid_start_x, 245)
\tcursor_rect.size = Vector2(210, 310)
\tadd_child(cursor_rect)
\t
\t# P1/P2 pick labels
\tp1_pick_label = Label.new()
\tp1_pick_label.text = "P1: ???"
\tp1_pick_label.position = Vector2(200, 700)
\tp1_pick_label.add_theme_font_size_override("font_size", 40)
\tadd_child(p1_pick_label)
\t
\tp2_pick_label = Label.new()
\tp2_pick_label.text = "CPU: ???"
\tp2_pick_label.position = Vector2(1400, 700)
\tp2_pick_label.add_theme_font_size_override("font_size", 40)
\tadd_child(p2_pick_label)
\t
\tstatus_label = Label.new()
\tstatus_label.text = "Use A/D to move, SPACE to confirm"
\tstatus_label.position = Vector2(960, 900)
\tstatus_label.add_theme_font_size_override("font_size", 28)
\tstatus_label.horizontal_alignment = HORIZONTAL_ALIGNMENT_CENTER
\tstatus_label.set_anchors_preset(Control.PRESET_CENTER_TOP)
\tadd_child(status_label)
\t
\tprint("Char select ready — ", len(characters), " fighters")

func _process(delta):
\tif transition_timer >= 0:
\t\ttransition_timer -= delta
\t\tif transition_timer <= 0:
\t\t\tvar main = get_parent()
\t\t\tif main and main.has_method("transition_to"):
\t\t\t\tmain.transition_to("fighting")
\t\treturn
\t
\tif not p1_confirmed:
\t\t# Cursor movement
\t\tif Input.is_action_just_pressed("ui_left") and cursor_index > 0:
\t\t\tcursor_index -= 1
\t\t\t_update_cursor()
\t\tif Input.is_action_just_pressed("ui_right") and cursor_index < len(characters) - 1:
\t\t\tcursor_index += 1
\t\t\t_update_cursor()
\t\t
\t\t# Confirm
\t\tif Input.is_action_just_pressed("ui_accept"):
\t\t\tp1_confirmed = true
\t\t\tp1_pick_label.text = "P1: " + characters[cursor_index].to_upper()
\t\t\tstatus_label.text = "CPU selecting..."
\t\t\tcursor_rect.color = Color(0, 1, 0, 0.3)
\t\t\tprint("P1 selected: ", characters[cursor_index])
\t
\telif not p2_confirmed:
\t\t# CPU auto-picks after delay
\t\tcpu_timer += delta
\t\tif cpu_timer >= 0.8:
\t\t\tvar cpu_pick = randi() % len(characters)
\t\t\tp2_confirmed = true
\t\t\tp2_pick_label.text = "CPU: " + characters[cpu_pick].to_upper()
\t\t\tstatus_label.text = "GET READY!"
\t\t\tprint("CPU selected: ", characters[cpu_pick])
\t\t\ttransition_timer = 1.5

func _update_cursor():
\tvar grid_start_x = (1920 - (len(characters) * 250)) / 2.0
\tcursor_rect.position.x = grid_start_x + cursor_index * 250 - 5
"""


def generate_scene_scripts(
    game_name: str,
    characters: list[str],
    scenes_dir: Path,
) -> list[Path]:
    """Generate all scene scripts. Returns list of paths."""
    scenes_dir.mkdir(parents=True, exist_ok=True)
    generated: list[Path] = []

    # Start screen
    path = scenes_dir / "start_screen.gd"
    path.write_text(generate_start_screen_script(game_name), encoding="utf-8")
    generated.append(path)

    # Char select
    path = scenes_dir / "char_select.gd"
    path.write_text(generate_char_select_script(game_name, characters), encoding="utf-8")
    generated.append(path)

    return generated
