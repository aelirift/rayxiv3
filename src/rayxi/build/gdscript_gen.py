"""GDScript generator — translates DAG EntityNodes into Godot scripts.

Takes a fighter's EntityNode (with all 386 properties) and generates
a complete .gd script with:
  - @export var for config properties (editable in Godot inspector)
  - var for state properties (runtime, initialized in _ready)
  - computed getters for derived properties
  - Animation list constant

This is deterministic — no LLM needed. The template defines the structure,
DLR fills the values, this generates the code.
"""

from __future__ import annotations

from pathlib import Path

from .dag import EntityNode, PropertyNode


# ---------------------------------------------------------------------------
# Type mapping: template types → GDScript types
# ---------------------------------------------------------------------------

_TYPE_MAP = {
    "int": "int",
    "float": "float",
    "bool": "bool",
    "string": "String",
    "String": "String",
    "Vector2": "Vector2",
    "array": "Array",
    "object": "Dictionary",
    "enum": "String",  # enums stored as strings in GDScript
}

# Properties that are Godot builtins — do NOT re-declare, set in _ready() instead
_GODOT_BUILTINS = {
    "position", "rotation", "scale", "visible", "modulate", "z_index",
    "velocity",  # CharacterBody2D
    "global_position", "global_rotation", "global_scale",
}

_DEFAULT_VALUES = {
    "int": "0",
    "float": "0.0",
    "bool": "false",
    "string": '""',
    "String": '""',
    "Vector2": "Vector2.ZERO",
    "array": "[]",
    "object": "{}",
    "enum": '""',
}


def _gd_type(template_type: str) -> str:
    return _TYPE_MAP.get(template_type, "Variant")


def _gd_default(prop: PropertyNode) -> str:
    """Get the GDScript default value for a property."""
    # Use filled value if available
    if prop.value:
        return _format_value(prop.value, prop.type)
    if prop.initial:
        return _format_value(prop.initial, prop.type)
    if prop.default:
        return _format_value(prop.default, prop.type)
    # Fall back to type default
    return _DEFAULT_VALUES.get(prop.type, "null")


def _format_value(raw: str, prop_type: str) -> str:
    """Format a raw value string as a GDScript literal."""
    raw = str(raw).strip()

    # Python→GDScript conversions
    if raw in ("True", "False"):
        return raw.lower()
    if raw.lower() in ("true", "false"):
        return raw.lower()
    if raw in ("None",) or raw.lower() in ("null", "none", ""):
        return _DEFAULT_VALUES.get(prop_type, "null")

    # Already a GDScript expression
    if raw.startswith("Vector2(") or raw.startswith("Color("):
        return raw

    # Object/dict
    if raw.startswith("{"):
        return raw

    # Array
    if raw.startswith("["):
        return raw

    # Numeric
    try:
        int(raw)
        return raw
    except ValueError:
        pass
    try:
        float(raw)
        return raw
    except ValueError:
        pass

    # Reference to another property (e.g. initial = "max_health")
    # Only treat as identifier reference for numeric types
    if prop_type in ("int", "float") and raw.isidentifier():
        return raw  # will be resolved in _ready()

    # String — always quote if not already quoted
    if not raw.startswith('"'):
        return f'"{raw}"'
    return raw


# ---------------------------------------------------------------------------
# Script generation
# ---------------------------------------------------------------------------

def generate_fighter_script(entity: EntityNode, godot_base: str = "CharacterBody2D") -> str:
    """Generate a complete .gd script for a fighter entity."""
    lines: list[str] = []

    lines.append(f"extends {godot_base}")
    lines.append(f"## Generated by RayXI for: {entity.name}")
    lines.append(f"## Role: {entity.role} | Properties: {len(entity.properties)}")
    lines.append("")

    # --- Config properties as @export ---
    config = entity.config_props
    state = entity.state_props
    derived = entity.derived_props

    # Separate builtins (set in _ready) from custom properties (declared)
    builtin_config = [p for p in config if p.name in _GODOT_BUILTINS]
    custom_config = [p for p in config if p.name not in _GODOT_BUILTINS]
    builtin_state = [p for p in state if p.name in _GODOT_BUILTINS]
    custom_state = [p for p in state if p.name not in _GODOT_BUILTINS]

    if custom_config:
        lines.append("# =========================================")
        lines.append("# CONFIG — set in inspector, constant at runtime")
        lines.append("# =========================================")
        lines.append("")

        # Group by source mechanic
        by_mechanic: dict[str, list[PropertyNode]] = {}
        for p in custom_config:
            by_mechanic.setdefault(p.source_mechanic, []).append(p)

        for mechanic, props in sorted(by_mechanic.items()):
            lines.append(f"# --- {mechanic} ---")
            for p in props:
                gd_t = _gd_type(p.type)
                gd_v = _gd_default(p)
                if gd_t != "Variant":
                    lines.append(f"@export var {p.name}: {gd_t} = {gd_v}")
                else:
                    lines.append(f"@export var {p.name} = {gd_v}")
            lines.append("")

    if custom_state:
        lines.append("# =========================================")
        lines.append("# STATE — changes at runtime")
        lines.append("# =========================================")
        lines.append("")

        by_mechanic = {}
        for p in custom_state:
            by_mechanic.setdefault(p.source_mechanic, []).append(p)

        for mechanic, props in sorted(by_mechanic.items()):
            lines.append(f"# --- {mechanic} ---")
            for p in props:
                gd_t = _gd_type(p.type)
                gd_v = _gd_default(p)
                if gd_t != "Variant":
                    lines.append(f"var {p.name}: {gd_t} = {gd_v}")
                else:
                    lines.append(f"var {p.name} = {gd_v}")
            lines.append("")

    if derived:
        lines.append("# =========================================")
        lines.append("# DERIVED — computed, never stored")
        lines.append("# =========================================")
        lines.append("")

        for p in derived:
            gd_t = _gd_type(p.type)
            formula = p.formula if p.formula else "0"
            # Convert Python-style division to GDScript
            formula = formula.replace("//", "/")
            lines.append(f"var {p.name}: {gd_t}:")
            lines.append(f"\tget: return {formula}")
            lines.append("")

    # --- _ready: initialize builtins + state from config ---
    ready_lines: list[str] = []

    # Builtin properties that need to be set in _ready
    for p in builtin_config + builtin_state:
        gd_v = _gd_default(p)
        ready_lines.append(f"\t{p.name} = {gd_v}")

    # State properties that reference config values (e.g. initial = "max_health")
    # Only for numeric types — string "idle" is a literal, not a reference
    for p in custom_state:
        if (p.initial and p.initial.isidentifier()
                and p.type in ("int", "float")
                and p.initial not in ("true", "false", "True", "False", "None", "null")
                and p.initial not in ("0", "0.0", "none")):
            ready_lines.append(f"\t{p.name} = {p.initial}")

    if ready_lines:
        lines.append("func _ready():")
        lines.extend(ready_lines)
        lines.append("")

    return "\n".join(lines)


def generate_projectile_script(entity: EntityNode, godot_base: str = "Area2D") -> str:
    """Generate a .gd script for a projectile entity."""
    lines: list[str] = []
    lines.append(f"extends {godot_base}")
    lines.append(f"## Generated by RayXI for: {entity.name}")
    lines.append("")

    config = [p for p in entity.config_props if p.name not in _GODOT_BUILTINS]
    state = [p for p in entity.state_props if p.name not in _GODOT_BUILTINS]
    builtin_props = [p for p in entity.properties if p.name in _GODOT_BUILTINS]

    if config:
        lines.append("# CONFIG")
        for p in config:
            gd_t = _gd_type(p.type)
            gd_v = _gd_default(p)
            lines.append(f"@export var {p.name}: {gd_t} = {gd_v}")
        lines.append("")

    if state:
        lines.append("# STATE")
        for p in state:
            gd_t = _gd_type(p.type)
            gd_v = _gd_default(p)
            lines.append(f"var {p.name}: {gd_t} = {gd_v}")
        lines.append("")

    # Builtins set in _ready
    if builtin_props:
        lines.append("func _ready():")
        for p in builtin_props:
            gd_v = _gd_default(p)
            lines.append(f"\t{p.name} = {gd_v}")
        lines.append("")

    return "\n".join(lines)


def save_entity_script(entity: EntityNode, output_dir: Path, godot_base: str = "") -> Path:
    """Generate and save a .gd script for an entity. Returns the output path.

    godot_base is a legacy override — prefer setting entity.godot_node_type in the DAG.
    """
    output_dir.mkdir(parents=True, exist_ok=True)
    base = godot_base or entity.godot_node_type

    if entity.role == "fighter":
        script = generate_fighter_script(entity, base or "CharacterBody2D")
    elif entity.role == "projectile":
        script = generate_projectile_script(entity, base or "Area2D")
    else:
        script = generate_fighter_script(entity, base or "Node2D")

    out_path = output_dir / f"{entity.name}.gd"
    out_path.write_text(script, encoding="utf-8")
    return out_path
