"""Godot 4 project generator — produces a complete project directory.

Generates:
  - project.godot (project file with main scene)
  - main.tscn (root scene with scene manager)
  - main.gd (scene transition FSM)
  - Per-scene .tscn files with node trees
  - Placeholder sprites (ColorRect nodes for fighters)
  - Attaches generated .gd scripts to nodes

The goal: `godot --path <dir>` opens without errors.
"""

from __future__ import annotations

import logging
from pathlib import Path

from rayxi.knowledge.mechanic_loader import ExpandedGameSchema
from rayxi.spec.models import GameIdentity, SceneManifest

from .dag import GameDAG

_log = logging.getLogger("rayxi.build.godot_project")


def generate_godot_project(
    dag: GameDAG,
    hlr: GameIdentity,
    schema: ExpandedGameSchema,
    manifest: SceneManifest,
    project_dir: Path,
) -> list[str]:
    """Generate a complete Godot 4 project. Returns list of generated file paths."""
    project_dir.mkdir(parents=True, exist_ok=True)
    generated: list[str] = []

    # project.godot
    generated.append(_write_project_file(project_dir, hlr))

    # main scene + script
    generated.append(_write_main_scene(project_dir, hlr))
    generated.append(_write_main_script(project_dir, hlr))

    # Scene scripts — real implementations for key scenes
    from .scene_scripts import generate_scene_scripts
    from .system_gen import generate_system_scripts, generate_fighting_scene_script

    characters = hlr.get_enum("characters")
    scenes_dir = project_dir / "scenes"
    scene_script_paths = generate_scene_scripts(hlr.game_name, characters, scenes_dir)
    for p in scene_script_paths:
        generated.append(str(p.relative_to(project_dir)))

    # Per-scene .tscn files
    dag_scene_names = {s.name for s in dag.scenes}
    for scene in dag.scenes:
        generated.append(_write_scene_tscn(project_dir, scene, dag, schema))

    # Stub .tscn for HLR scenes not in the DAG
    from .dag import SceneNode
    for hlr_scene in hlr.scenes:
        if hlr_scene.scene_name not in dag_scene_names:
            stub = SceneNode(name=hlr_scene.scene_name, purpose=hlr_scene.purpose)
            generated.append(_write_scene_tscn(project_dir, stub, dag, schema))
            # Only generate stub .gd if we don't already have a real one
            gd_path = scenes_dir / f"{hlr_scene.scene_name}.gd"
            if not gd_path.exists():
                generated.append(_write_scene_script(project_dir, stub, dag))

    # System scripts
    all_systems = set()
    for scene in dag.scenes:
        all_systems.update(scene.active_systems)
    systems_dir = project_dir / "scripts" / "systems"
    system_paths = generate_system_scripts(dag, list(all_systems), systems_dir)
    for p in system_paths:
        generated.append(str(p.relative_to(project_dir)))

    # Fighting scene script (wires up systems)
    fighting_scenes = [s for s in dag.scenes if "fight" in s.name and "intro" not in s.name]
    if fighting_scenes:
        fight_path = generate_fighting_scene_script(
            dag, fighting_scenes[0].active_systems, scenes_dir)
        generated.append(str(fight_path.relative_to(project_dir)))

    # Character scripts (already generated by gdscript_gen, just ensure they're in place)
    char_dir = project_dir / "scripts" / "characters"
    char_dir.mkdir(parents=True, exist_ok=True)
    for name in dag.fighter_entities:
        gd_path = char_dir / f"{name}.gd"
        if gd_path.exists():
            generated.append(str(gd_path.relative_to(project_dir)))

    # Projectile scripts
    proj_dir = project_dir / "scripts" / "projectiles"
    proj_dir.mkdir(parents=True, exist_ok=True)
    for name in dag.projectile_entities:
        gd_path = proj_dir / f"{name}.gd"
        if gd_path.exists():
            generated.append(str(gd_path.relative_to(project_dir)))

    _log.info("Godot project: %d files generated at %s", len(generated), project_dir)
    return generated


# ---------------------------------------------------------------------------
# project.godot
# ---------------------------------------------------------------------------

def _write_project_file(project_dir: Path, hlr: GameIdentity) -> str:
    content = f"""; Engine configuration file.
; Generated by RayXI for: {hlr.game_name}

[application]

config/name="{hlr.game_name}"
run/main_scene="res://main.tscn"
config/features=PackedStringArray("4.3")

[display]

window/size/viewport_width=1920
window/size/viewport_height=1080
window/size/mode=3
window/stretch/mode="viewport"

[rendering]

renderer/rendering_method="gl_compatibility"
"""
    path = project_dir / "project.godot"
    path.write_text(content, encoding="utf-8")
    return str(path.relative_to(project_dir))


# ---------------------------------------------------------------------------
# main.tscn + main.gd — scene manager
# ---------------------------------------------------------------------------

def _write_main_scene(project_dir: Path, hlr: GameIdentity) -> str:
    content = """[gd_scene load_steps=2 format=3]

[ext_resource type="Script" path="res://main.gd" id="1"]

[node name="Main" type="Node2D"]
script = ExtResource("1")
"""
    path = project_dir / "main.tscn"
    path.write_text(content, encoding="utf-8")
    return str(path.relative_to(project_dir))


def _write_main_script(project_dir: Path, hlr: GameIdentity) -> str:
    """Generate the main scene manager that handles scene transitions."""
    scenes = [s.scene_name for s in hlr.scenes]
    fsm_states = hlr.global_fsm.states

    # Build scene path mapping
    scene_map_lines = []
    for s in scenes:
        scene_map_lines.append(f'\t"{s}": preload("res://scenes/{s}.tscn"),')

    content = f"""extends Node2D
## Main scene manager — handles scene transitions.
## Generated by RayXI for: {hlr.game_name}

var scene_map: Dictionary = {{
{chr(10).join(scene_map_lines)}
}}

var current_scene_name: String = ""
var current_scene_node: Node = null

func _ready():
\tload_scene("{scenes[0]}")

func load_scene(scene_name: String):
\tif current_scene_node:
\t\tcurrent_scene_node.queue_free()
\tif scene_name in scene_map:
\t\tcurrent_scene_name = scene_name
\t\tcurrent_scene_node = scene_map[scene_name].instantiate()
\t\tadd_child(current_scene_node)
\t\tprint("Loaded scene: ", scene_name)
\telse:
\t\tpush_error("Unknown scene: " + scene_name)

func transition_to(scene_name: String):
\tload_scene(scene_name)
"""
    path = project_dir / "main.gd"
    path.write_text(content, encoding="utf-8")
    return str(path.relative_to(project_dir))


# ---------------------------------------------------------------------------
# Per-scene .tscn + .gd
# ---------------------------------------------------------------------------

def _write_scene_tscn(
    project_dir: Path,
    scene,  # SceneNode from DAG
    dag: GameDAG,
    schema: ExpandedGameSchema,
) -> str:
    """Generate a .tscn file for one scene with placeholder nodes."""
    scenes_dir = project_dir / "scenes"
    scenes_dir.mkdir(parents=True, exist_ok=True)

    # Build the scene resource
    ext_resources: list[str] = []
    nodes: list[str] = []
    ext_id = 0

    # Scene script
    ext_id += 1
    scene_script_id = ext_id
    ext_resources.append(
        f'[ext_resource type="Script" path="res://scenes/{scene.name}.gd" id="{ext_id}"]'
    )

    # Root node
    nodes.append(f'[node name="{scene.name}" type="Node2D"]')
    nodes.append(f'script = ExtResource("{scene_script_id}")')

    # Background (ColorRect as placeholder)
    nodes.append(f'')
    nodes.append(f'[node name="Background" type="ColorRect" parent="."]')
    nodes.append(f'offset_right = 1920.0')
    nodes.append(f'offset_bottom = 1080.0')
    nodes.append(f'color = Color(0.15, 0.15, 0.2, 1)')

    # Add role slot nodes (fighters)
    for slot in scene.role_slots:
        nodes.append(f'')
        nodes.append(f'[node name="{slot.slot_name}" type="CharacterBody2D" parent="."]')
        if slot.slot_name == "p1_fighter":
            nodes.append(f'position = Vector2(480, 912)')
        elif slot.slot_name == "p2_fighter":
            nodes.append(f'position = Vector2(1440, 912)')

        # Placeholder sprite (colored rectangle — roughly 35% screen height)
        color = "Color(0.2, 0.5, 1, 1)" if "p1" in slot.slot_name else "Color(1, 0.3, 0.2, 1)"
        nodes.append(f'')
        nodes.append(f'[node name="Sprite" type="ColorRect" parent="{slot.slot_name}"]')
        nodes.append(f'offset_left = -48.0')
        nodes.append(f'offset_top = -192.0')
        nodes.append(f'offset_right = 48.0')
        nodes.append(f'offset_bottom = 0.0')
        nodes.append(f'color = {color}')

    # Add HUD entities
    for entity in scene.entities:
        if entity.role == "hud":
            nodes.append(f'')
            if "health" in entity.name:
                # Health bar
                nodes.append(f'[node name="{entity.name}" type="ProgressBar" parent="."]')
                x = 60 if "p1" in entity.name else 1140
                nodes.append(f'offset_left = {x}.0')
                nodes.append(f'offset_top = 30.0')
                nodes.append(f'offset_right = {x + 720}.0')
                nodes.append(f'offset_bottom = 70.0')
                nodes.append(f'value = 100.0')
            elif "timer" in entity.name:
                # Timer label
                nodes.append(f'[node name="{entity.name}" type="Label" parent="."]')
                nodes.append(f'offset_left = 910.0')
                nodes.append(f'offset_top = 20.0')
                nodes.append(f'offset_right = 1010.0')
                nodes.append(f'offset_bottom = 80.0')
                nodes.append(f'text = "99"')
                nodes.append(f'horizontal_alignment = 1')
            else:
                nodes.append(f'[node name="{entity.name}" type="Label" parent="."]')
                nodes.append(f'text = "{entity.name}"')

    # Compose the .tscn file
    load_steps = len(ext_resources) + 1
    header = f"[gd_scene load_steps={load_steps} format=3]\n\n"
    resources = "\n".join(ext_resources) + "\n\n"
    node_text = "\n".join(nodes) + "\n"

    content = header + resources + node_text
    path = scenes_dir / f"{scene.name}.tscn"
    path.write_text(content, encoding="utf-8")
    return str(path.relative_to(project_dir))


def _write_scene_script(project_dir: Path, scene, dag: GameDAG) -> str:
    """Generate a placeholder .gd script for one scene."""
    scenes_dir = project_dir / "scenes"
    scenes_dir.mkdir(parents=True, exist_ok=True)

    systems_comment = ", ".join(scene.active_systems) if scene.active_systems else "none"

    content = f"""extends Node2D
## Scene: {scene.name} — {scene.purpose}
## Active systems: {systems_comment}
## Generated by RayXI

func _ready():
\tprint("Scene ready: {scene.name}")

func _process(delta):
\tpass  # TODO: system processing loop
"""
    path = scenes_dir / f"{scene.name}.gd"
    path.write_text(content, encoding="utf-8")
    return str(path.relative_to(project_dir))
