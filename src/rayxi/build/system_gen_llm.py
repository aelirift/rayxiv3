def _extract_slice_properties(slice_data: dict) -> set[str]:
    """Return all property names the slice authorizes this system to touch.

    Pulls from property_details (the expanded slice B), owned_properties,
    own_writes.target, own_reads.source, peer_writes, and downstream_reads.
    Strips the `owner.` prefix so we end up with the bare field name.

    The expanded slice (`property_details`) is the authoritative source; the
    legacy fields are still scanned for backwards compatibility.
    """
    names: set[str] = set()

    def _add(ref: str) -> None:
        # `fighter.current_action` → `current_action`
        if "." in ref:
            names.add(ref.split(".", 1)[1])
        else:
            names.add(ref)

    # Primary source: property_details from the expanded slice.
    for pid in slice_data.get("property_details", {}) or {}:
        _add(pid)
    for node in slice_data.get("owned_properties", []):
        _add(node.get("name", ""))
        _add(node.get("id", ""))
    for edge in slice_data.get("own_writes", []):
        _add(edge.get("target", ""))
    for edge in slice_data.get("own_reads", []):
        _add(edge.get("source", ""))
    for prop_id in slice_data.get("peer_writes", {}):
        _add(prop_id)
    for prop_id in slice_data.get("downstream_reads", {}):
        _add(prop_id)
    return {n for n in names if n}


def _extract_enum_domains(slice_data: dict) -> dict[str, set[str]]:
    """Map `owner.field_name` → set of allowed enum string values.

    Used by the property validator to catch cases where the LLM writes a
    string literal comparison like `if fighter.current_action == "walking"`
    — where "walking" isn't in the canonical enum. Any string literal
    assignment or comparison against an enum-valued property must use one
    of the declared values verbatim.
    """
    out: dict[str, set[str]] = {}
    details = slice_data.get("property_details", {}) or {}
    for pid, detail in details.items():
        values = detail.get("enum_values")
        if values and isinstance(values, list):
            out[pid] = set(values)
    return out


def _entity_variable_names(gd_source: str, pool_names: list[str] | None) -> set[str]:
    """Collect local var / param names that clearly bind to entities so the
    property validator doesn't flag `config.max_health` or `systems.foo`.

    Heuristic: look at `for <name> in entity_pools["fighters"]` patterns and
    `<name>:` type annotations referencing CharacterBody2D/Node2D/etc.
    """
    names: set[str] = {"fighter", "attacker", "defender", "opponent", "projectile",
                       "p1", "p2", "f", "fighter1", "fighter2"}
    for m in re.finditer(r'for\s+(\w+)\s+in\s+entity_pools\[', gd_source):
        names.add(m.group(1))
    for m in re.finditer(r'for\s+(\w+)\s+in\s+fighters', gd_source):
        names.add(m.group(1))
    return names


def _strip_comments_and_strings(gd_source: str) -> tuple[str, list[str]]:
    """Return (code_without_comments_or_strings, extracted_string_literals).

    Used so property-access scanning doesn't trip on identifiers inside
    string literals or comments, while enum-domain scanning has the full
    list of string literals that the LLM emitted.
    """
    no_comments = []
    for line in gd_source.splitlines():
        # Strip `#` comments (respecting `#` inside string literals — simple
        # heuristic: only drop if `#` comes before any `"`).
        idx = line.find("#")
        if idx >= 0:
            # If there's no `"` before the `#`, assume it's a comment.
            quote_before = line.find('"')
            if quote_before == -1 or idx < quote_before:
                line = line[:idx]
        no_comments.append(line)
    body = "\n".join(no_comments)

    # Extract all double-quoted string literals.
    strings = re.findall(r'"((?:\\"|[^"])*)"', body)
    # Mask them out so subsequent scans don't match identifiers inside.
    masked = re.sub(r'"(?:\\"|[^"])*"', '""', body)
    return masked, strings


def _validate_property_access(
    gd_source: str,
    slice_data: dict,
    pool_names: list[str] | None,
) -> tuple[bool, str]:
    """Check every entity property access in the generated code is authorized
    by the slice. Also catches enum-drift — string literals compared against
    enum-valued properties that aren't in the canonical value set.

    Two classes of error caught:
      1. Invented property access: `fighter.input_horizontal` when slice has
         no such property. Causes runtime "Invalid access" errors.
      2. Invented enum value: `if fighter.current_action == "walking"` when
         the canonical set is `["idle", "walk_forward", "walk_back", ...]`.
         Causes silent gameplay bugs (the condition is never true).
    """
    authorized = _extract_slice_properties(slice_data) | _NATIVE_MEMBERS
    enum_domains = _extract_enum_domains(slice_data)
    entity_vars = _entity_variable_names(gd_source, pool_names)
    masked_body, string_literals = _strip_comments_and_strings(gd_source)

    errors: list[str] = []

    # --- 1) Property access drift ---
    invalid_props: list[tuple[str, str]] = []
    seen_props: set[tuple[str, str]] = set()
    for pattern in (_ENTITY_ACCESS_RE, _ENTITY_SUBSCRIPT_RE):
        for m in pattern.finditer(masked_body):
            obj, prop = m.group(1), m.group(2)
            if obj not in entity_vars:
                continue
            if prop in authorized:
                continue
            if prop.startswith("_"):
                continue
            key = (obj, prop)
            if key in seen_props:
                continue
            seen_props.add(key)
            invalid_props.append(key)
            if len(invalid_props) >= 8:
                break
        if len(invalid_props) >= 8:
            break

    if invalid_props:
        errors.append("Invented entity properties not in the slice:")
        for obj, prop in invalid_props:
            errors.append(f"  - {obj}.{prop}")
        errors.append(
            "You may ONLY read/write properties that appear in property_details "
            "in the slice. If you need cross-system state, use the canonical "
            "property in the impact map (e.g. fighter.current_action for "
            "input→movement). NEVER invent fighter.input_horizontal, "
            "fighter.input_jump, player_slot, position_x, is_in_hitstun, etc."
        )

    # --- 2) Enum value drift ---
    # Scan for `<entity_var>.<enum_prop>` comparisons and assignments; the
    # value must be one of the declared enum strings.
    _SENTINEL_NAMES = ("none", "idle", "off", "inactive", "unset", "default")

    def _suggest_fix(literal: str, allowed: set[str]) -> str:
        """Suggest the closest valid enum value for a drift case."""
        # Empty string → the canonical "unset" sentinel (none/idle/off).
        if literal == "":
            for name in _SENTINEL_NAMES:
                if name in allowed:
                    return f'"{name}"'
        # Substring match: "walk" → "walk_forward"
        lit_lower = literal.lower()
        for val in sorted(allowed):
            if lit_lower and lit_lower in val.lower():
                return f'"{val}"'
        # Fallback: first value in the allowed set.
        return f'"{sorted(allowed)[0]}"' if allowed else "?"

    enum_violations: list[str] = []
    for pid, allowed_values in enum_domains.items():
        if "." not in pid:
            continue
        owner, prop = pid.split(".", 1)
        cmp_re = re.compile(
            rf'\b(\w+)\.{re.escape(prop)}\s*(?:==|!=|=)\s*"([^"]*)"'
        )
        for m in cmp_re.finditer(masked_body):
            var_name, literal = m.group(1), m.group(2)
            if var_name not in entity_vars:
                continue
            if literal not in allowed_values:
                suggestion = _suggest_fix(literal, allowed_values)
                enum_violations.append(
                    f"  - {var_name}.{prop} uses \"{literal}\" — "
                    f"USE {suggestion} INSTEAD. "
                    f"Full enum: {sorted(allowed_values)}"
                )
            if len(enum_violations) >= 8:
                break
        if len(enum_violations) >= 8:
            break

    if enum_violations:
        if errors:
            errors.append("")
        errors.append("Enum-value drift — string literals don't match the canonical set:")
        errors.extend(enum_violations)
        errors.append(
            "CRITICAL: NEVER use empty string \"\" or null for enum properties. "
            "If the enum has a 'none'/'idle'/'off' value, use THAT as the "
            "explicit unset state. If you're comparing for 'no value', compare "
            "against the canonical unset string — not against \"\"."
        )

    if not errors:
        return True, ""
    return False, "\n".join(["contract violation:", *errors])


def _validate_contract(gd_source: str) -> tuple[bool, str]:
    """Static check for the setup/process signatures the scene_gen loop depends on.

    godot --check-only only proves the file PARSES; it does not prove the LLM
    honored the contract. Without this the emitted fighting.gd will crash at
    runtime on calls to a misshapen setup(). We regex-match the two signatures
    and feed the error back into the retry loop.
    """
    if not _SETUP_SIG_RE.search(gd_source):
        return False, (
            "contract violation: missing or wrong `func setup(pools: Dictionary, "
            "cfg: Dictionary = {})` — scene_gen needs this exact shape"
        )
    if not _PROCESS_SIG_RE.search(gd_source):
        return False, (
            "contract violation: missing `func process(delta: float) -> void` — "
            "scene_gen calls this every physics frame"
        )
    return True, ""


async def _generate_system_via_llm(
    system: str,
    slice_data: dict,
    spec: MechanicSpec | None,
    hlr: GameIdentity,
    caller: LLMCaller,
    constants: dict | None = None,
    pool_names: list[str] | None = None,
    system_description: str | None = None,
    max_retries: int = 3,
) -> str:
    """Call the LLM to produce a system .gd file. Validates + retries on errors."""
    key = _cache_key(system, slice_data, spec, constants, pool_names, system_description)
    cached = _cache_get(key)
    trace = get_trace()
    caller_name = type(caller).__name__
    label = f"system_gen[{system}]"
    if cached:
        _log.info("%s — cache hit", label)
        if trace:
            cid = trace.llm_start("codegen", label, caller_name, 0)
            trace.llm_end(cid, output_chars=len(cached), cache_hit=True)
        return cached

    hlr_ctx = {
        "game_name": hlr.game_name,
        "genre": hlr.genre,
        "global_rules": hlr.global_rules,
    }

    base_prompt_parts = [
        f"## Game context\n```json\n{json.dumps(hlr_ctx, indent=2)}\n```",
        f"## System to implement: **{system}**",
    ]
    if system_description:
        base_prompt_parts.append(
            "## System responsibilities (from KB template)\n"
            "This is the prose description of what this system must do — the slice "
            "below is the data-flow graph, but THIS text tells you the behavior. "
            "Your implementation must honor every effect and interaction listed here.\n\n"
            f"> {system_description}"
        )
    if pool_names:
        base_prompt_parts.append(
            "## Available entity pools\n"
            "Read entities from `entity_pools[<name>]`. These are the pool names "
            "available for THIS system (populated by scene_gen at setup time):\n\n"
            + "\n".join(f"- `{p}`" for p in pool_names)
        )

    # Enum-valued properties get their own call-out so the LLM cannot miss
    # the canonical string set. Any `fighter.current_action`-style handoff
    # with enum_values must use EXACTLY these strings.
    property_details = slice_data.get("property_details", {}) or {}
    _SENTINELS = ("none", "idle", "off", "inactive", "unset", "default")
    enum_lines: list[str] = []
    for pid, detail in sorted(property_details.items()):
        values = detail.get("enum_values")
        if not values:
            continue
        # Flag the unset sentinel (if any) so the LLM knows what "no value" looks like.
        sentinel = next((v for v in values if v.lower() in _SENTINELS), None)
        header = f"- **`{pid}`** (type `{detail['type']}`) must be one of:"
        if sentinel:
            header += f"  ← unset value is `\"{sentinel}\"` (use this for no-state, NEVER `\"\"`)"
        enum_lines.append(header)
        enum_lines.extend(f"    - `{v}`" for v in values)
    if enum_lines:
        base_prompt_parts.append(
            "## Enum-valued properties — use EXACTLY these string values\n"
            "These properties have a constrained value domain declared in the impact "
            "map. When you write a string to one of these, use ONE of the listed values "
            "verbatim. When you read from one of these, your pattern matches must use "
            "the same values. NEVER invent values like `\"walk\"` when the enum says "
            "`\"walk_forward\"`, and NEVER use empty string `\"\"` — use the explicit "
            "unset sentinel (`\"none\"`, `\"idle\"`, etc.) when the enum declares one.\n\n"
            + "\n".join(enum_lines)
        )

    base_prompt_parts.append(
        f"## Impact-map slice for this system\n```json\n{json.dumps(slice_data, indent=2, default=str)}\n```"
    )
    if constants:
        base_prompt_parts.append(
            "## Constants (DLR-filled) — declare one @export var per entry, default = value\n"
            "```json\n" + json.dumps(constants, indent=2, default=str) + "\n```\n"
            "In `setup()`, copy each constant from `cfg` if present, else keep the default."
        )
    if spec is not None:
        base_prompt_parts.append(
            f"## Mechanic spec (custom feature definition)\n```json\n{spec.model_dump_json(indent=2)}\n```"
        )
    base_prompt = "\n\n".join(base_prompt_parts)

    last_err: str = ""
    for attempt in range(max_retries):
        cid = trace.llm_start("codegen", label, caller_name, len(base_prompt)) if trace else ""
        try:
            prompt = base_prompt
            if last_err:
                prompt = (
                    f"Your previous attempt failed validation with this error:\n\n"
                    f"```\n{last_err}\n```\n\n"
                    f"Fix the error and emit a corrected, complete script.\n\n{base_prompt}"
                )
            raw = await caller(_SYSTEM_PROMPT, prompt, json_mode=False, label=label)
            gd = _strip_markdown_fences(raw)
            if not gd.startswith("extends"):
                raise RuntimeError(f"output does not start with 'extends': {gd[:80]!r}")

            ok, err = _godot_check_script(gd)
            if not ok:
                last_err = err
                _log.warning("%s attempt %d: godot check failed: %s", label, attempt + 1, err[:200])
                if trace:
                    trace.llm_end(cid, output_chars=len(gd), error=err[:120])
                continue
            contract_ok, contract_err = _validate_contract(gd)
            if not contract_ok:
                last_err = contract_err
                _log.warning("%s attempt %d: %s", label, attempt + 1, contract_err)
                if trace:
                    trace.llm_end(cid, output_chars=len(gd), error=contract_err[:120])
                continue
            prop_ok, prop_err = _validate_property_access(gd, slice_data, pool_names)
            if not prop_ok:
                last_err = prop_err
                _log.warning("%s attempt %d: property drift — %s",
                             label, attempt + 1, prop_err.splitlines()[0])
                if trace:
                    trace.llm_end(cid, output_chars=len(gd), error=prop_err[:120])
                continue
            _cache_put(key, gd)
            if trace:
                trace.llm_end(cid, output_chars=len(gd))
            return gd
        except Exception as exc:
            last_err = str(exc)
            _log.warning("%s attempt %d crashed: %s", label, attempt + 1, str(exc)[:200])
            if trace:
                trace.llm_end(cid, output_chars=0, error=str(exc)[:120])
    raise RuntimeError(f"{label} failed after {max_retries} attempts: {last_err}")


async def generate_system_via_llm(
    system: str,
    imap: ImpactMap,
    hlr: GameIdentity,
    caller: LLMCaller | None = None,
    constants: dict | None = None,
    pool_names: list[str] | None = None,
    system_description: str | None = None,
) -> str:
    """Generate one system .gd via LLM. Module-level entry."""
    if caller is None:
        router = build_router(build_callers())
        caller = router.get("mlr_interactions")

    slice_data = imap.slice_for_system(system)
    spec = next((m for m in hlr.mechanic_specs if m.system_name == system), None)
    return await _generate_system_via_llm(
        system, slice_data, spec, hlr, caller,
        constants=constants,
        pool_names=pool_names,
        system_description=system_description,
    )
