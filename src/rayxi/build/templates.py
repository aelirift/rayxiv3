"""GDScript templates for deterministic entity codegen.

Each template maps a Godot node type to a GDScript file generator.
Takes DLR property_values and produces a complete .gd script.

Only used for simple/passive entities (HUD, background, UI).
Characters and game_objects with action_sets go through LLM builder.
"""

from __future__ import annotations

# ---------------------------------------------------------------------------
# Value converters — DLR string values → GDScript literals
# ---------------------------------------------------------------------------


def _to_gdscript_value(raw: str, prop_type: str = "") -> str:
    """Convert a DLR property value string to a GDScript literal."""
    raw = raw.strip()

    # Already looks like a GDScript literal
    if raw.startswith("Vector2(") or raw.startswith("Color("):
        return raw

    # Boolean
    if raw.lower() in ("true", "false"):
        return raw.lower()

    # None/null
    if raw.lower() in ("null", "none"):
        return "null"

    # Numeric
    try:
        int(raw)
        return raw
    except ValueError:
        pass
    try:
        float(raw)
        return raw
    except ValueError:
        pass

    # Color hex string
    if raw.startswith("#") and len(raw) in (4, 7, 9):
        return f'Color("{raw}")'

    # File path (resource)
    if raw.startswith("res://") or raw.endswith((".png", ".jpg", ".tres", ".tscn")):
        return f'preload("{raw}")'

    # Vector2 from "x, y" format
    if "," in raw and prop_type.lower() in ("vector2", ""):
        parts = raw.split(",")
        if len(parts) == 2:
            try:
                x, y = float(parts[0].strip()), float(parts[1].strip())
                return f"Vector2({x}, {y})"
            except ValueError:
                pass

    # Default: treat as string
    return f'"{raw}"'


# ---------------------------------------------------------------------------
# Property mapping — DLR names → Godot property/setter
# ---------------------------------------------------------------------------

# Special properties that need setter calls instead of direct assignment
_SETTER_PROPERTIES = {
    "font_size": 'add_theme_font_size_override("font_size", {value})',
    "font_color": 'add_theme_color_override("font_color", {value})',
    "bg_color": 'add_theme_stylebox_override("panel", _make_stylebox({value}))',
}

# Properties that map to compound assignments
_COMPOUND_PROPERTIES = {
    "width": ("size.x", "float"),
    "height": ("size.y", "float"),
    "position_x": ("position.x", "float"),
    "position_y": ("position.y", "float"),
    "scale_x": ("scale.x", "float"),
    "scale_y": ("scale.y", "float"),
}


def _property_to_assignment(name: str, raw_value: str, prop_type: str = "") -> str:
    """Convert a DLR property to a GDScript assignment line."""
    value = _to_gdscript_value(raw_value, prop_type)

    # Setter call
    if name in _SETTER_PROPERTIES:
        return "\t" + _SETTER_PROPERTIES[name].format(value=value)

    # Compound property (width → size.x)
    if name in _COMPOUND_PROPERTIES:
        gdprop, _ = _COMPOUND_PROPERTIES[name]
        return f"\t{gdprop} = {value}"

    # Direct assignment
    return f"\t{name} = {value}"


# ---------------------------------------------------------------------------
# Template: generates full .gd file content
# ---------------------------------------------------------------------------


def generate_entity_script(
    godot_node_type: str,
    entity_name: str,
    property_values: dict[str, str],
    property_types: dict[str, str] | None = None,
) -> str:
    """Generate a complete GDScript file for a deterministic entity.

    Args:
        godot_node_type: Godot class name (Label, ProgressBar, Sprite2D, etc.)
        entity_name: snake_case entity name
        property_values: {property_name: value_as_string} from DLR
        property_types: {property_name: type_string} from MLR (optional, helps conversion)

    Returns:
        Complete .gd file content.
    """
    ptypes = property_types or {}
    lines: list[str] = []

    # Header
    lines.append(f"extends {godot_node_type}")
    lines.append(f"## Auto-generated by RayXI deterministic codegen for: {entity_name}")
    lines.append("")

    # Export vars for properties that might be tweaked in editor
    exported: list[str] = []
    for name, value in property_values.items():
        gd_value = _to_gdscript_value(value, ptypes.get(name, ""))
        # Skip properties that use setters or are compound
        if name in _SETTER_PROPERTIES or name in _COMPOUND_PROPERTIES:
            continue
        # Skip Godot built-in properties (set in _ready)
        if name in ("position", "size", "scale", "rotation", "visible",
                     "modulate", "z_index", "text", "value", "max_value",
                     "min_value", "texture"):
            continue
        exported.append(f"@export var {name} = {gd_value}")

    if exported:
        for line in exported:
            lines.append(line)
        lines.append("")

    # _ready() — set all properties
    lines.append("func _ready():")
    if not property_values:
        lines.append("\tpass")
    else:
        for name, value in property_values.items():
            lines.append(_property_to_assignment(name, value, ptypes.get(name, "")))

    lines.append("")
    return "\n".join(lines)


